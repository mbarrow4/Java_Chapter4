/**Simple program that loops through FIVE*/

public class For_Loop 
{
	public static void main(String[] args) 
	{
		/**REQUIREMENTS for a FOR-LOOP*/
		//1. Initialized control variable
		//2. Control condition
		//3. Incremented
		for(int count=0; count<=5; count++) 
		{
			System.out.println(count);
		}
		
		/**for (initialization; Loop Condition , incrementer)*/

	}//End of Main

}//End of Class
