/**A nested for loop that prints stars*/
public class Nested_For_Loops 
{

	public static void main(String[] args) 
	{
		for(int i=1; i<=5; i++) 
		{
			for(int j=1; j<=3; j++) 
			{
				for(int k=1; k <=4; k++) 
				{
					System.out.print("*");
				}
				System.out.println();
			}
			System.out.println();
		}

	}//End of MAIN

}//End of CLASS
